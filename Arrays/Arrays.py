from numpy import *

arr1 = array([1, 2, 3, 4, 5])      #Normal Arrray
print(arr1)
print()

arr2 = linspace(1, 20, 30)            #Start, Stop, Number of Parts
print(arr2)
print()

arr3 = logspace(1, 20, 30)                 #Start, Stop, Number of Parts with log
print(arr3)
print()

arr4 = arange(1, 10, 2)                   #Start, Stop, Steps
print(arr4)